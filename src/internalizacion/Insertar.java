    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        int id, unidades;
        float precio;
        try {
            id = Integer.parseInt(jTextField1.getText());
                if (id <= 0) {
                    this.setEnabled(false);
                    Error error = new Error(this, 10, 2);
                    error.setVisible(true);
                    return;
                } else {
                    boolean encontrado = false;
                    for (int i = 0; i < inventario.size(); ++i) {
                        if(id == inventario.get(i).getId()){
                            encontrado = true;
                            Error error = new Error(this, 10, 2);
                            error.setVisible(true);
                            return;
                        }
                    }
                }
        } catch(NumberFormatException e) {
            this.setEnabled(false);
            Error error = new Error(this, 10, 2);
            error.setVisible(true);
            return;
        }
        String nombre = jTextField2.getText();
        nombre = nombre.replaceAll("\\s+","");

        if (nombre.isEmpty()) {
            this.setEnabled(false);
            Error error = new Error(this, 16, 2);
            error.setVisible(true);
            return;
        }
        try {
            unidades = Integer.parseInt(jTextField3.getText());
            if (unidades < 0) {
                this.setEnabled(false);
                Error error = new Error(this, 12, 2);
                error.setVisible(true);
                return;
            } 
        } catch(NumberFormatException e) {
            this.setEnabled(false);
            Error error = new Error(this, 12, 2);
            error.setVisible(true);
            return;
        }
        try {
            precio = Float.parseFloat(jTextField4.getText());
            if (precio < 0) {
                this.setEnabled(false);
                Error error = new Error(this, 12, 2);
                error.setVisible(true);
                return;
            } 
        } catch(NumberFormatException e) {
            this.setEnabled(false);
            Error error = new Error(this, 12, 2);
            error.setVisible(true);
            return;
        }
        jLabel1.setText(datos[idioma][15]);
        Fruta fruta = new Fruta(id, nombre, unidades, precio);
        inventario.add(fruta);
    }//GEN-LAST:event_jButton1ActionPerformed

